  required List<Map<String, dynamic>> vocabulary,
  required List<Map<String, String>> examples,
  required String speaking,
  required String writing,
  required String summary,
  required String previousId,
  required String fillQuestion,
  required String fillAnswer,
  required String arrangeQuestion,
  required String arrangeAnswer,
  required String truthQuestion,
  required String truthAnswer,
}) {
  final first = vocabulary.first;
  final second = vocabulary[1];
  final exercises = [
    exercise(
      '$id-p01',
      'multipleChoice',
      '“${first['englishWord']}” maxay ka dhigan tahay?',
      [
        first['somaliMeaning'] as String,
        second['somaliMeaning'] as String,
        'Tiro',
        'Meel',
      ],
      first['somaliMeaning'] as String,
      'Jawaabta saxda ahi waa “${first['somaliMeaning']}”; waa macnaha ${first['englishWord']} ee casharkan.',
    ),
    exercise(
      '$id-p02',
      'fillInTheBlank',
      fillQuestion,
      [fillAnswer, 'name', 'small', 'sound'],
      fillAnswer,
      'Erayga “$fillAnswer” ayaa jumladda macne sax ah siinaya.',
    ),
    exercise(
      '$id-p03',
      'matchWords',
      'Isku aadi ${second['englishWord']} iyo macnihiisa.',
      [
        second['somaliMeaning'] as String,
        first['somaliMeaning'] as String,
        'Salaan',
        'Mahadsanid',
      ],
      second['somaliMeaning'] as String,
      '${second['englishWord']} waxaa loola jeedaa ${second['somaliMeaning']}.',
    ),
    exercise(
      '$id-p04',
      'arrangeSentence',
      arrangeQuestion,
      const [],
      arrangeAnswer,
      'Kala horreynta saxda ahi waa “$arrangeAnswer”.',
    ),
    exercise(
      '$id-p05',
      'englishToSomali',
      'Turjun: ${examples.first['english']}',
      [
        examples.first['somali']!,
        examples[1]['somali']!,
        'Magacaygu waa Cali.',
        'Waxaan aadayaa suuqa.',
      ],
      examples.first['somali']!,
      'Turjumaadda saxda ahi waxay gudbinaysaa macnaha jumladda oo dhan.',
    ),
    exercise(
      '$id-p06',
      'somaliToEnglish',
      'U beddel English: ${examples[1]['somali']}',
      [
        examples[1]['english']!,
        examples.first['english']!,
        'I go to school.',
        'This is my phone.',
      ],
      examples[1]['english']!,
      'Jumladda “${examples[1]['english']}” ayaa si dabiici ah u gudbinaysa macnaha Soomaaliga.',
    ),
    exercise(
      '$id-p07',
      'trueFalse',
      truthQuestion,
      const ['True', 'False'],
      truthAnswer,
      truthAnswer == 'True'
          ? 'Weedhu waa sax marka la raaco waxa casharka lagu bartay.'
          : 'Weedhu waa khalad; dib u eeg xeerka iyo tusaalooyinka casharka.',
    ),
    exercise(
      '$id-p08',
      'speakingPrompt',
      speaking,
      const [],
      'Teacher review',
      'Si cod dheer u hadal. Ujeeddadu waa dhawaaq cad iyo kalsooni, mana aha xawaare.',
    ),
    exercise(
      '$id-p09',
      'shortWriting',
      writing,
      const [],
      'Teacher review',
      'Qoraalkaaga hubi: xaraf sax ah, kala horreyn sax ah iyo calaamadda dhammaadka.',
    ),
  ];
  return {
    'id': id,
    'levelId': 'A1',
    'unitId': 'a1-u01',
    'lessonNumber': number,
    'titleEnglish': englishTitle,
    'titleSomali': somaliTitle,
    'shortDescriptionSomali': description,
    'learningObjectives': objectives,
    'lessonType': type,
    'estimatedMinutes': type == 'review' ? 20 : 15,
    'difficulty': 'beginner',
    'isLocked': number != 1,
    'requiredPreviousLessonId': previousId.isEmpty ? null : previousId,
    'vocabulary': vocabulary,
    'grammar': null,
    'examples': examples,
    'practiceExercises': exercises,
    'speakingPractice': speaking,
    'writingPractice': writing,
    'summarySomali': summary,
    'quizQuestions': exercises.take(3).toList(),
  };
}

