import 'dart:convert';
import 'dart:io';

Map<String, dynamic> word(
  String english,
  String somali,
  String part,
  String pronunciation,
  String exampleEnglish,
  String exampleSomali,
  String explanation, [
  String mistake = '',
]) => {
  'englishWord': english,
  'somaliMeaning': somali,
  'partOfSpeech': part,
  'pronunciation': pronunciation,
  'exampleEnglish': exampleEnglish,
  'exampleSomali': exampleSomali,
  'explanationSomali': explanation,
  'commonMistakeSomali': mistake,
};

Map<String, String> example(String english, String somali) => {
  'english': english,
  'somali': somali,
};

Map<String, dynamic> exercise(
  String id,
  String type,
  String question,
  List<String> options,
  String answer,
  String explanation,
) => {
  'id': id,
  'type': type,
  'question': question,
  'options': options,
  'correctAnswer': answer,
  'explanationSomali': explanation,
};

Map<String, dynamic> lesson({
  required String id,
  required int number,
  required String englishTitle,
  required String somaliTitle,
  required String description,
